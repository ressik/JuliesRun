			<h3><a href="#">What is JDRF?</a></h3>
				<div>
					JDRF is the leading global organization focused on juvenile diabetes also known as type 1 diabetes (T1D) research. 
					Driven by passionate, grassroots volunteers connected to children, adolescents, and adults with this disease, 
					JDRF is now the largest charitable supporter of T1D research. 
					The goal of JDRF research is to improve the lives of all people affected by T1D by accelerating progress on the 
					most promising opportunities for curing, better treating, and preventing T1D. 
					JDRF collaborates with a wide spectrum of partners who share this goal.
					<p/>
					Since its founding in 1970, JDRF has awarded more than $1.6 billion to diabetes research. Past JDRF efforts 
					have helped to significantly advance the care of people with this disease, and have expanded the critical 
					scientific understanding of T1D. JDRF will not rest until T1D is fully conquered. More than 80 percent of 
					JDRF's expenditures directly support research and research-related education.
				</div>
			<h3><a href="#">What is Type 1 Diabetes?</a></h3>
				<div>
					Type 1 diabetes is usually diagnosed in children and young adults, and was previously known as
					juvenile diabetes. In type 1 diabetes, the body does not produce insulin.
					<p/>
					Insulin is a hormone that is needed to convert sugar, starches and other food into energy needed
					for daily life. Only 5% of people with diabetes have this form of the disease. 
					<p/>
					To live long and healthy lives children and adults with type 1 diabetes need to monitor blood 
					sugars several times throughout the day (via finger prick/blood samples), and have insulin 
					administered every time they eat or drink anything (via injections or an insulin pump). 
					<p/>
					Complications include Heart Disease, Stroke, Blindness, Kidney Disease, Nervous System Disease, Amputation, Seizures, Coma and Death.
				</div>
